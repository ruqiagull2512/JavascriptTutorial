// switch
 const day = "friday"
 switch (day) {
    case  "monday":
        console.log("Today is Monday");
         break;
    case "tuesday":
        console.log("Today is Tuesday");
         break;
    case "wednesday":
        console.log("Today is wednesday");
         break;
    case "friday":
        console.log("Today is friday");
         break;
    case "saturday":
        console.log("Today is saturday");
         break;
 
    default:
        console.log("default");


        break;
 }